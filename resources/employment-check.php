        <h4><u>Employment Check</u></h4><br>
        <p>The scope and coverage might differ from each company but the check could range from checking your latest employer to checking all listed employers in the past 10 years. The information being checked is also different each company but the most common information that are validated would be:</p>
        <ul>
            <li>If you have actually been employed in the company you have declared</li>
            <li>Inclusive dates of employment and if you are still active in the company system</li>
