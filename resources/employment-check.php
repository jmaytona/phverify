        <h4><u>How is the employment being conducted?</u></h4><br>
        <p>The employment can either be done by the company or can be endorsed to a third-party background check vendor.</p>
        <p>Most common verification process done is by collecting previous employment documents like Certificate of Employment, Resignation Letter or completed Clearance forms. The collected documents can either be authenticated by reaching out to the previous employers or accepted at face value.</p>
        <p>Another way to do the employment check is by asking candidates to fill-out a form outlining the employment details that needs to be verified along with a letter of consent or authorization so the company or a third-party vendor can check on your behalf. This can be done by either</p>
        <ul>
            <li>Calling the HR representative or your previous supervisor, </li>
            <li>Sending a verification request to the company or HR email or</li>
            <li>By personally visiting the site to conduct the verification</li>
        </ul><br><br>

        <h4><u>How can you be cleared in the employment check?</u></h4><br>
        <p>You can be cleared in the employment check if all declared information matches the information that was verified by the company. You can also be cleared if your previous employer has not claimed any derogatory findings against you.</p>
        <p>For companies that does not conduct actual employment verification, submission of employment documents (Certificate of employment) for all declared companies might suffice.</p><br><br>

        <h4><u>What can you do if there are negative or incomplete findings in your employment check?</u></h4><br>
        <p>The most common negative finding for the employment check are discrepancies between information provided and the information that they have validated from your previous employers. Examples would be incorrect start date, incorrect end date or incorrect position in the company. This can usually be corrected by submitting official documents that you have received from the company that matches the information that you have declared.</p>
        <p> If discrepancy was because of an error on your part, the company would usually ask you for a written explanation on why there was a discrepancy in the first place. E.g. you don’t remember the exact employment dates or documents that have these are not readily available so you had to rely on what you can remember. While this might be acceptable for some companies, please not that some might construe this discrepancy as fraud or a deliberate attempt to mis declare information.</p>
        <p>Another negative finding for employment is when your previous employers shared derogatory records against you like termination due to AWOL, FRAUD etc. This also might include cases where your previous employer declared you as not eligible for rehire. Since each company would have different internal policies regarding, you might not be able to get cleared in the employment checks. You might also need to provide supporting documents that would help you in disproving the records that was declared or documents that would show that you have been cleared already with the records against you.</p>
        <p>Finally, your employment check might also be tagged as incomplete or unverified if you employers is not able to confirm or verify the information details. Some companies might not have a details verification process or the HR employees might not be consistent in verifying your declared information so you might be tagged as not completed. For instances like this – you can either provide your certificate of employment as a supporting document to show the you are cleared or you can coordinate with your previous company directly and share with your new employer how to verify the information.</p><br><br>

