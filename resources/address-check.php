        <h4><u>What is the address check for? </u></h4><br>
        <p>The address check is usually conducted to get the complete information about your current home location. Typically, a company archives the information in your 201 filing in case important employment letters needs to be sent. The company might also use the information as a supplementary data for your identity check or as an actual requirement by the company that can be used to check for the addresses that you have declared in your other documents.</p><br><br>
        
        <h4><u>How is the address check being conducted?</u></h4><br>
        <p>The address check is usually conducted by either the recruiter/HR employee or thru a third party background check vendor. The company might ask you to fill out your complete address and/or relevant documents that shows your current address. They might also require you to submit a sketch or map from their office to your current home.</p>
        <p>With the information provided, they can either:</p>
        <ol>
            <li>Use your name and matching it thru existing database that contains address</li>
            <li>Collect and check government IDs or Billing documents to confirm current address</li>
            <li>Confirm your residence by calling the barangay or community office</li>
            <li>Conducting ocular inspection by going to the listed address and confirming if applicant is residing in the given location</li>
        </ol><br><br>
        <h4><u>How can you be cleared in the address check?</u></h4><br>
        <p>You can be cleared in the address check if the company was able to verify that you are indeed residing in your address that you declare</p><br><br>
        <p>The usual contents of the background investigation form are dependent on what checks are required by the company however the standard information that’s always present in the form is the letter of authorization that you need to sign so that the employers would have your consent to conduct the background check.</p><br><br>
        
        <h4><u>What can you do if there are negative or incomplete findings in your address check?</u></h4><br>
        <p>The most common negative finding for the address check is if your confirmed address is now aligned with what you have declared. One way to correct this is by submitting other official or legal documents that contains your current home address like government IDs or proof of billing for utilities. If your home was not found during the ocular or site visit, you can submit a sketch or map that can indicate notable landmarks to help with the search.</p><br>
        <p>However, if the negative finding is indeed valid because you are no longer residing in your declared address (e.g. you declared your parent’s address, you are renting, you recently moved etc), you need to talk to your recruiter/HR employee to check if you can update the address that you provided.</p><br><br>

